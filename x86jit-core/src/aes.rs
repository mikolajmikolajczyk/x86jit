/// ShiftRows byte permutation on the flat little-endian state: `out[i] = in[PERM[i]]`.
const SHIFT_ROWS: [usize; 16] = [0, 5, 10, 15, 4, 9, 14, 3, 8, 13, 2, 7, 12, 1, 6, 11];
/// InvShiftRows: the inverse permutation of `SHIFT_ROWS`.
const INV_SHIFT_ROWS: [usize; 16] = [0, 13, 10, 7, 4, 1, 14, 11, 8, 5, 2, 15, 12, 9, 6, 3];

#[inline]
fn to_bytes(x: u128) -> [u8; 16] {
    x.to_le_bytes()
}

#[inline]
fn from_bytes(b: [u8; 16]) -> u128 {
    u128::from_le_bytes(b)
}

/// GF(2^8) multiply by 2 (`xtime`), reduced by the AES polynomial 0x11B.
#[inline]
fn xtime(a: u8) -> u8 {
    let hi = a & 0x80;
    let s = a << 1;
    if hi != 0 {
        s ^ 0x1b
    } else {
        s
    }
}

/// GF(2^8) multiply of `a` by `b`, reduced by 0x11B (carryless mul + reduce).
/// Shared with the GFNI primitives (`gfni.rs`, task-210).
#[inline]
pub(crate) fn gmul(mut a: u8, mut b: u8) -> u8 {
    let mut p = 0u8;
    for _ in 0..8 {
        if b & 1 != 0 {
            p ^= a;
        }
        a = xtime(a);
        b >>= 1;
    }
    p
}

fn sub_bytes(s: [u8; 16]) -> [u8; 16] {
    let mut o = [0u8; 16];
    for i in 0..16 {
        o[i] = SBOX[s[i] as usize];
    }
    o
}

fn inv_sub_bytes(s: [u8; 16]) -> [u8; 16] {
    let mut o = [0u8; 16];
    for i in 0..16 {
        o[i] = INV_SBOX[s[i] as usize];
    }
    o
}

fn shift_rows(s: [u8; 16]) -> [u8; 16] {
    let mut o = [0u8; 16];
    for i in 0..16 {
        o[i] = s[SHIFT_ROWS[i]];
    }
    o
}

fn inv_shift_rows(s: [u8; 16]) -> [u8; 16] {
    let mut o = [0u8; 16];
    for i in 0..16 {
        o[i] = s[INV_SHIFT_ROWS[i]];
    }
    o
}

fn mix_columns(s: [u8; 16]) -> [u8; 16] {
    let mut o = [0u8; 16];
    for c in 0..4 {
        let b = c * 4;
        let (s0, s1, s2, s3) = (s[b], s[b + 1], s[b + 2], s[b + 3]);
        o[b] = xtime(s0) ^ (xtime(s1) ^ s1) ^ s2 ^ s3;
        o[b + 1] = s0 ^ xtime(s1) ^ (xtime(s2) ^ s2) ^ s3;
        o[b + 2] = s0 ^ s1 ^ xtime(s2) ^ (xtime(s3) ^ s3);
        o[b + 3] = (xtime(s0) ^ s0) ^ s1 ^ s2 ^ xtime(s3);
    }
    o
}

fn inv_mix_columns(s: [u8; 16]) -> [u8; 16] {
    let mut o = [0u8; 16];
    for c in 0..4 {
        let b = c * 4;
        let (s0, s1, s2, s3) = (s[b], s[b + 1], s[b + 2], s[b + 3]);
        o[b] = gmul(s0, 14) ^ gmul(s1, 11) ^ gmul(s2, 13) ^ gmul(s3, 9);
        o[b + 1] = gmul(s0, 9) ^ gmul(s1, 14) ^ gmul(s2, 11) ^ gmul(s3, 13);
        o[b + 2] = gmul(s0, 13) ^ gmul(s1, 9) ^ gmul(s2, 14) ^ gmul(s3, 11);
        o[b + 3] = gmul(s0, 11) ^ gmul(s1, 13) ^ gmul(s2, 9) ^ gmul(s3, 14);
    }
    o
}

/// `aesenc state, rk`: `MixColumns(ShiftRows(SubBytes(state))) XOR rk`.
pub fn aes_enc(state: u128, rk: u128) -> u128 {
    let t = mix_columns(shift_rows(sub_bytes(to_bytes(state))));
    from_bytes(t) ^ rk
}

/// `aesenclast state, rk`: `ShiftRows(SubBytes(state)) XOR rk` (no MixColumns).
pub fn aes_enc_last(state: u128, rk: u128) -> u128 {
    let t = shift_rows(sub_bytes(to_bytes(state)));
    from_bytes(t) ^ rk
}

/// `aesdec state, rk`: `InvMixColumns(InvShiftRows(InvSubBytes(state))) XOR rk`.
pub fn aes_dec(state: u128, rk: u128) -> u128 {
    let t = inv_mix_columns(inv_sub_bytes(inv_shift_rows(to_bytes(state))));
    from_bytes(t) ^ rk
}

/// `aesdeclast state, rk`: `InvShiftRows(InvSubBytes(state)) XOR rk` (no InvMixColumns).
pub fn aes_dec_last(state: u128, rk: u128) -> u128 {
    let t = inv_shift_rows(inv_sub_bytes(to_bytes(state)));
    from_bytes(t) ^ rk
}

/// `aesimc x`: `InvMixColumns(x)`.
pub fn aes_imc(x: u128) -> u128 {
    from_bytes(inv_mix_columns(to_bytes(x)))
}

/// `aeskeygenassist x, imm8` (Intel SDM):
/// * `RESULT[31:0]   = SubWord(X1)`
/// * `RESULT[63:32]  = RotWord(SubWord(X1)) XOR RCON`
/// * `RESULT[95:64]  = SubWord(X3)`
/// * `RESULT[127:96] = RotWord(SubWord(X3)) XOR RCON`
///
/// where `X1 = x[63:32]` (dword 1), `X3 = x[127:96]` (dword 3), `RCON = imm8`,
/// `SubWord` applies the S-box to each byte, and `RotWord([b0,b1,b2,b3])`
/// rotates to `[b1,b2,b3,b0]` (a rotate of the 32-bit word right by 8 bits).
pub fn aes_keygen(x: u128, imm: u8) -> u128 {
    let dw = |i: u32| (x >> (32 * i)) as u32;
    let sub_word = |w: u32| {
        let b = w.to_le_bytes();
        u32::from_le_bytes([
            SBOX[b[0] as usize],
            SBOX[b[1] as usize],
            SBOX[b[2] as usize],
            SBOX[b[3] as usize],
        ])
    };
    // RotWord: [b0,b1,b2,b3] -> [b1,b2,b3,b0] == rotate the 32-bit word right by 8.
    let rot_word = |w: u32| w.rotate_right(8);

    let sw1 = sub_word(dw(1));
    let sw3 = sub_word(dw(3));
    let rcon = imm as u32;
    let r0 = sw1;
    let r1 = rot_word(sw1) ^ rcon;
    let r2 = sw3;
    let r3 = rot_word(sw3) ^ rcon;
    (r0 as u128) | ((r1 as u128) << 32) | ((r2 as u128) << 64) | ((r3 as u128) << 96)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn fips197_appendix_b_round1() {
        // FIPS-197 Appendix B: after AddRoundKey the state (column-major, stored
        // little-endian byte i = row i%4, col i/4) is:
        //   19 a0 9a e9 / 3d f4 c6 f8 / e3 e2 8d 48 / be 2b 2a 08
        let state: [u8; 16] = [
            0x19, 0x3d, 0xe3, 0xbe, 0xa0, 0xf4, 0xe2, 0x2b, 0x9a, 0xc6, 0x8d, 0x2a, 0xe9, 0xf8,
            0x48, 0x08,
        ];
        // Round key for round 1 (Appendix B, w[4..8]):
        //   a0 88 23 2a / fa 54 a3 6c / fe 2c 39 76 / 17 b1 39 05
        let rk: [u8; 16] = [
            0xa0, 0xfa, 0xfe, 0x17, 0x88, 0x54, 0x2c, 0xb1, 0x23, 0xa3, 0x39, 0x39, 0x2a, 0x6c,
            0x76, 0x05,
        ];
        // Expected state after round 1 (Appendix B "start of round 2"):
        //   a4 68 6b 02 / 9c 9f 5b 6a / 7f 35 ea 50 / f2 2b 43 49
        let expected: [u8; 16] = [
            0xa4, 0x9c, 0x7f, 0xf2, 0x68, 0x9f, 0x35, 0x2b, 0x6b, 0x5b, 0xea, 0x43, 0x02, 0x6a,
            0x50, 0x49,
        ];
        let got = aes_enc(from_bytes(state), from_bytes(rk));
        assert_eq!(to_bytes(got), expected);
    }

    #[test]
    fn enc_dec_inverse() {
        // aesdeclast(aesenclast(s, 0), 0) with InvMix/Mix removed should invert
        // the last-round transform: aesdeclast∘aesenclast == identity for rk=0.
        let s = 0x0123_4567_89ab_cdef_fedc_ba98_7654_3210u128;
        let e = aes_enc_last(s, 0);
        let d = aes_dec_last(e, 0);
        assert_eq!(d, s);
    }

    #[test]
    fn imc_is_inverse_of_mix() {
        let s = 0x0011_2233_4455_6677_8899_aabb_ccdd_eeffu128;
        let mixed = from_bytes(mix_columns(to_bytes(s)));
        assert_eq!(aes_imc(mixed), s);
    }
}
